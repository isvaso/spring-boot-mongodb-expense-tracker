package com.isvaso.springbootmongodbexpensetracker.model;

/**
 * Represents the category of an expense.
 * This enum defines various categories that can be associated with an expense, such as entertainment, utilities, groceries, etc.
 */
public enum ExpenseCategory {

    /**
     * Category: Entertainment
     */
    ENTERTAINMENT,

    /**
     * Category: Utilities
     */
    UTILITIES,

    /**
     * Category: Groceries
     */
    GROCERIES,

    /**
     * Category: Miscellaneous
     */
    MISC,

    /**
     * Category: Restaurant
     */
    RESTAURANT;

}
